var landscape = function() {
  var result = "";
  var flat = function(size) {
    for (var count = 0; count < size; count++)
      result += "_";
  };
  var mountain = function(size) {
    result += "/";
    for (var count = 0; count < size; count++)
      result += "'";
    result += "\\";
  };

  flat(7);
  mountain(40);
  flat(6);
  mountain(11);
  flat(20);
  return result;
};

console.log(landscape());